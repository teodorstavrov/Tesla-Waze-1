export default async function handler(req: VercelRequest, res: VercelResponse) {
  res.setHeader('Access-Control-Allow-Origin', '*')
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS')
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type, Authorization')

  if (req.method === 'OPTIONS') { res.status(204).end(); return }
  if (req.method !== 'POST')    { res.status(405).json({ error: 'Method not allowed' }); return }

  const authHeader = String(req.headers['authorization'] ?? '')
  const token      = authHeader.startsWith('Bearer ') ? authHeader.slice(7) : ''
  const adminToken = process.env['ADMIN_TOKEN']

  if (!adminToken || !token || token !== adminToken) {
    return res.status(401).json({ error: 'Unauthorized' })
  }

  let added = 0
  let skipped = 0

  for (const cam of CAMERAS) {
    // Stable ID based on coordinates — prevents duplicates on repeated calls
    const id = `cam-${cam.lat.toFixed(6)}-${cam.lng.toFixed(6)}`
    try {
      // Check if already exists
      const existing = await eventsStore.getAll()
      if (existing.find((e) => e.id === id)) { skipped++; continue }

      await eventsStore.add({
        id,
        type:          'camera',
        lat:           cam.lat,
        lng:           cam.lng,
        timestamp:     Date.now(),
        confirmations: 0,
      })
      added++
    } catch (err) {
      console.error('[seed-cameras] error adding', id, err)
    }
  }

  return res.status(200).json({
    ok: true,
    added,
    skipped,
    total: CAMERAS.length,
  })
}
